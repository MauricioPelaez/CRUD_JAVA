package valueobject;

/**
 *
 * @author hearf
 */
public class AutobusVO {
    
    private int id;
    private String estado;
    private String matricula;
    private String capacidad;
    private String soat;
    private String tecnomecanica;
    private String tarjetaOperacional;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(String capacidad) {
        this.capacidad = capacidad;
    }

    public String getSoat() {
        return soat;
    }

    public void setSoat(String soat) {
        this.soat = soat;
    }

    public String getTecnomecanica() {
        return tecnomecanica;
    }

    public void setTecnomecanica(String tecnomecanica) {
        this.tecnomecanica = tecnomecanica;
    }

    public String getTarjetaOperacional() {
        return tarjetaOperacional;
    }

    public void setTarjetaOperacional(String tarjetaOperacional) {
        this.tarjetaOperacional = tarjetaOperacional;
    }


 
    
    
}

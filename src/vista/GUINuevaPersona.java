    private void txtPrimerNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrimerNombreKeyTyped
       
        char v = evt.getKeyChar();
       
        if((v<'a'||v>'z')&&(v<'A'||v>'Z'))evt.consume();
        if(Character.isLowerCase(v)){//Todo lo que ingresa se pone en mayúscula
            String cad=(""+v).toUpperCase();
            v=cad.charAt(0);
            evt.setKeyChar(v);
        }    
    }//GEN-LAST:event_txtPrimerNombreKeyTyped


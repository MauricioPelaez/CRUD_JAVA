      private void cargarTipoDocumento() {
        listaTipoDocumento = fachada.consultarTipoDocumento();
        if (listaTipoDocumento != null) {
            for (TipoDocumentoVO tipoDocumentoVO : listaTipoDocumento) {
                comboTipoDocumento.addItem(tipoDocumentoVO.getNombre());
            }
        }
    }
      
      private void cargarEstadoCivil() {
        listaEstadoCivil = fachada.consultarEstadoCivil();
        if (listaEstadoCivil != null) {
            for (EstadoCivilVO estadoCivilVO : listaEstadoCivil) {
                comboEstadoCivil.addItem(estadoCivilVO.getNombre());
            }
        }
    }
      
       private void cargarCiudad() {
        listaCiudad = fachada.consultarListaCiudad(PROPERTIES);
        if (listaCiudad != null) {
            for (CiudadVO ciudadVO : listaCiudad) {
                comboCiudadNacimiento.addItem(ciudadVO.getNombre());
                comboCiudadExpedicion.addItem(ciudadVO.getNombre());
            }
        }
    }
      
